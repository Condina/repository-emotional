package br.com.emotional.entidade;

public class Emocao {

	private int id_emocao; 
	private String nome;
	
	public int getId_emocao() {
		return id_emocao;
	}
	public void setId_emocao(int id_emocao) {
		this.id_emocao = id_emocao;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	} 
}
